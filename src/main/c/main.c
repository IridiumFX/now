/* ---- Alforno-based project templates for `now init` ---- */

/* Template pastlet: project descriptor with {variable} placeholders.
   @vars provides defaults; the CLI overrides them via a second input. */
static const char NOW_INIT_TEMPLATE[] =
    "@vars {\n"
    "  group:    \"com.example\",\n"
    "  artifact: \"myproject\",\n"
    "  version:  \"0.1.0\",\n"
    "  lang:     \"c\",\n"
    "  std:      \"c11\",\n"
    "  type:     \"executable\"\n"
    "}\n"
    "@project {\n"
    "  group:    \"{group}\",\n"
    "  artifact: \"{artifact}\",\n"
    "  version:  \"{version}\",\n"
    "  langs:    [\"{lang}\"],\n"
    "  std:      \"{std}\",\n"
    "  output:   { type: \"{type}\", name: \"{artifact}\" },\n"
    "  compile:  { warnings: [\"Wall\", \"Wextra\"] },\n"
    "  deps:     []\n"
    "}\n";

/*
 * Render a now.pasta project descriptor using alforno templates.
 *
 * The template contains {variable} placeholders. CLI arguments are passed
 * as a @vars override pastlet that merges with (and overrides) the template
 * defaults. Alforno's Pass 1 (parameterize) resolves all variables.
 *
 * Returns a malloc'd string (Pasta-formatted), or NULL on error.
 */
static char *now_init_render(const char *group, const char *artifact,
                              const char *version, const char *lang,
                              const char *std_str, const char *type) {
    AlfResult ar;
    AlfContext *ctx = alf_create(ALF_AGGREGATE, &ar);
    if (!ctx) return NULL;

    /* Input 1: template with defaults */
    if (alf_add_input(ctx, NOW_INIT_TEMPLATE, strlen(NOW_INIT_TEMPLATE), &ar) != 0) {
        alf_free(ctx);
        return NULL;
    }

    /* Input 2: CLI overrides as @vars (last-write-wins merges with template @vars) */
    char vars[512];
    snprintf(vars, sizeof(vars),
        "@vars {\n"
        "  group:    \"%s\",\n"
        "  artifact: \"%s\",\n"
        "  version:  \"%s\",\n"
        "  lang:     \"%s\",\n"
        "  std:      \"%s\",\n"
        "  type:     \"%s\"\n"
        "}\n",
        group, artifact, version, lang, std_str, type);

    if (alf_add_input(ctx, vars, strlen(vars), &ar) != 0) {
        alf_free(ctx);
        return NULL;
    }

    /* Process: parameterize (resolve {vars}), merge, link */
    PastaValue *output = alf_process(ctx, &ar);
    alf_free(ctx);
    if (!output) return NULL;

    /* Extract the @project section and serialize it as a standalone file.
       Comment header is prepended manually. */
    const PastaValue *proj = pasta_map_get(output, "project");
    if (!proj) {
        pasta_free(output);
        return NULL;
    }

    char *body = pasta_write(proj, PASTA_PRETTY);
    pasta_free(output);
    if (!body) return NULL;

    /* Prepend a comment header */
    size_t hdr_len = 128 + strlen(lang);
    size_t total = hdr_len + strlen(body) + 1;
    char *result = (char *)malloc(total);
    if (!result) { free(body); return NULL; }

    int pos = snprintf(result, total,
        "; Generated by: now init %s\n"
        "; Edit this file to configure your project.\n",
        lang);
    strcat(result + pos, body);
    free(body);
    return result;
}

